#
# Usage Examples:
#   cd a        # Go to data directory
#   ls f        # List figures
#   vim p/*.Rmd # Edit paper files
#
# Safety: Removes existing symlinks first to avoid conflicts
# Tracking: All created symlinks are tracked in manifest for uninstall
create_symbolic_links() {
    log_info "Creating symbolic links for convenience..."
    
    # Remove existing symlinks to avoid conflicts
    # Only remove if they are actually symbolic links (not files or directories)
    local symlinks=("a" "n" "f" "t" "s" "m" "e" "o" "c" "p")
    for link in "${symlinks[@]}"; do
        if [[ -L "$link" ]]; then
            rm "$link"
            log_info "Removed existing symlink: $link"
        fi
    done
    
    # Create new symlinks using exact same approach as original zzcollab.sh
    # This preserves the exact behavior and avoids associative array issues
    local created_count=0
    
    # Create symlinks one by one (same as original)
    if [[ -d "./data" ]]; then
        ln -s ./data a && track_symlink "a" "./data" && log_info "Created symlink: a â†’ ./data" && ((created_count++))
    fi
    
    if [[ -d "./analysis" ]]; then
        ln -s ./analysis n && track_symlink "n" "./analysis" && log_info "Created symlink: n â†’ ./analysis" && ((created_count++))
    fi
    
    if [[ -d "./analysis/figures" ]]; then
        ln -s ./analysis/figures f && track_symlink "f" "./analysis/figures" && log_info "Created symlink: f â†’ ./analysis/figures" && ((created_count++))
    fi
    
    if [[ -d "./analysis/tables" ]]; then
        ln -s ./analysis/tables t && track_symlink "t" "./analysis/tables" && log_info "Created symlink: t â†’ ./analysis/tables" && ((created_count++))
    fi
    
    if [[ -d "./scripts" ]]; then
        ln -s ./scripts s && track_symlink "s" "./scripts" && log_info "Created symlink: s â†’ ./scripts" && ((created_count++))
    fi
    
    if [[ -d "./man" ]]; then
        ln -s ./man m && track_symlink "m" "./man" && log_info "Created symlink: m â†’ ./man" && ((created_count++))
    fi
    
    if [[ -d "./tests" ]]; then
        ln -s ./tests e && track_symlink "e" "./tests" && log_info "Created symlink: e â†’ ./tests" && ((created_count++))
    fi
    
    if [[ -d "./docs" ]]; then
        ln -s ./docs o && track_symlink "o" "./docs" && log_info "Created symlink: o â†’ ./docs" && ((created_count++))
    fi
    
    if [[ -d "./archive" ]]; then
        ln -s ./archive c && track_symlink "c" "./archive" && log_info "Created symlink: c â†’ ./archive" && ((created_count++))
    fi
    
